package Practice_5X.Homework5_2;
import java.awt.*;
public class Rect extends Shape
{
    public Rect(int x, int y)
    {
        super(x, y);
    }
    @Override
    public Color getColor()
    {
        return super.getColor();
    }
    @Override
    public int getX()
    {
        return super.getX();
    }
    @Override
    public int getY()
    {
        return super.getY();
    }
    @Override
    public void setX(int x)
    {
        super.setX(x);
    }
    @Override
    public void setY(int y)
    {
        super.setY(y);
    }
}